package com.dreams.designpattern.iterator;

/**
 * 实体类
 */
public class Concrete {

    private String no;
    private String name;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
